package main;

public class Main {
	private int Year;
	private int Month;
	private int Day;
	private String Time;
	public int getYear() {
		return Year;
	}
	public void setYear(int year) {
		Year = year;
	}
	public int getMonth() {
		return Month;
	}
	public void setMonth(int month) {
		Month = month;
	}
	public int getDay() {
		return Day;
	}
	public void setDay(int day) {
		Day = day;
	}
	public String gettime() {
		return Time;
	}
	public void setTime(String time) {
		Time = time;
	}
	
}
